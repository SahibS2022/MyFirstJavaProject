package learningInheritance;

public class Animal {

	void walk() {
		System.out.println("Sleeping Process");
	}

	public void eat() {
		System.out.println("Eating Process");
	}

	protected void sleep() {
		System.out.println("Sleeping Process");
	}

	private void run() {
		System.out.println("Sleeping Process");
	}
}
